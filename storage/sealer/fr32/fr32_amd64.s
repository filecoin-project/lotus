	// Pre-compute all 3 boundary bytes from input before any stores
	MOVBLZX 31(SI), R8
	MOVBLZX 32(SI), R9
	SHLL $6, R9
	ORL R9, R8
	// R8 = byte 31 value

	MOVBLZX 63(SI), R11
	MOVBLZX 64(SI), R12
	SHRL $2, R11
	SHLL $4, R12
	ORL R12, R11
	// R11 = byte 63 value

	MOVBLZX 95(SI), R13
	MOVBLZX 96(SI), R14
	SHRL $4, R13
	SHLL $2, R14
	ORL R14, R13
