TEXT ·padScalar(SB), NOSPLIT, $0-48
	MOVQ in_base+0(FP), SI
	MOVQ out_base+24(FP), DI
	MOVQ out_len+32(FP), CX
	SHRQ $7, CX
	TESTQ CX, CX
	JZ padScalarDone
	MOVQ $0x3FFFFFFFFFFFFFFF, R10

padScalarChunk:
	MOVOU 0(SI), X0
	MOVOU X0, 0(DI)
	MOVOU 15(SI), X0
	MOVOU X0, 15(DI)
	MOVBLZX 31(SI), R9
	ANDL $0x3f, R9
	MOVB R9, 31(DI)

	// Group 1: shift-2, bytes 32..63 (interleaved pairs)
	MOVQ 31(SI), R8
	MOVQ 32(SI), R9
	MOVQ 39(SI), R11
	MOVQ 40(SI), R12
	SHRQ $6, R8
	SHLQ $2, R9
	SHRQ $6, R11
	SHLQ $2, R12
	ORQ R9, R8
	ORQ R12, R11
	MOVQ R8, 32(DI)
	MOVQ R11, 40(DI)

	MOVQ 47(SI), R8
	MOVQ 48(SI), R9
	MOVQ 55(SI), R11
	MOVQ 56(SI), R12
	SHRQ $6, R8
	SHLQ $2, R9
	SHRQ $6, R11
	SHLQ $2, R12
	ORQ R9, R8
	ORQ R12, R11
	MOVQ R8, 48(DI)
	ANDQ R10, R11
	MOVQ R11, 56(DI)

	// Group 2: shift-4, bytes 64..95 (interleaved pairs)
	MOVQ 63(SI), R8
	MOVQ 64(SI), R9
	MOVQ 71(SI), R11
	MOVQ 72(SI), R12
	SHRQ $4, R8
	SHLQ $4, R9
	SHRQ $4, R11
	SHLQ $4, R12
	ORQ R9, R8
	ORQ R12, R11
	MOVQ R8, 64(DI)
	MOVQ R11, 72(DI)

	MOVQ 79(SI), R8
	MOVQ 80(SI), R9
	MOVQ 87(SI), R11
	MOVQ 88(SI), R12
	SHRQ $4, R8
	SHLQ $4, R9
	SHRQ $4, R11
	SHLQ $4, R12
	ORQ R9, R8
	ORQ R12, R11
	MOVQ R8, 80(DI)
	ANDQ R10, R11
	MOVQ R11, 88(DI)

	// Group 3: shift-6, bytes 96..127 (interleaved pairs)
	MOVQ 95(SI), R8
	MOVQ 96(SI), R9
	MOVQ 103(SI), R11
	MOVQ 104(SI), R12
	SHRQ $2, R8
	SHLQ $6, R9
	SHRQ $2, R11
	SHLQ $6, R12
	ORQ R9, R8
	ORQ R12, R11
	MOVQ R8, 96(DI)
	MOVQ R11, 104(DI)

	MOVQ 111(SI), R8
	MOVQ 112(SI), R9
	MOVQ 119(SI), R11
	SHRQ $2, R8
	SHLQ $6, R9
	SHRQ $2, R11
	ORQ R9, R8
	MOVQ R8, 112(DI)
	MOVQ R11, 120(DI)

	ADDQ $127, SI
	ADDQ $128, DI
	DECQ CX
	JNZ padScalarChunk

padScalarDone:
	RET

// padScalarNT uses MOVNTIQ for all stores, avoiding write-allocate traffic.
// The pad output stride is 128 bytes (exactly 2 cache lines), so WC buffers
// coalesce correctly. ALL stores must be NT to avoid evicting WC entries.
// Byte 31 is folded into the qword at offset 24 to avoid any temporal store.
