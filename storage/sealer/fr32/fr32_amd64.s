// padAVX512 keeps the hot loop compact: two 32-byte AVX512BW repack stores
// plus the naturally short scalar tail that avoids reading beyond 127 input bytes.
TEXT ·padAVX512(SB), NOSPLIT, $0-48
	MOVQ in_base+0(FP), SI
	MOVQ out_base+24(FP), DI
	MOVQ out_len+32(FP), CX
	MOVQ $-1, AX
	KMOVQ AX, K1
	MOVQ $0x7fffffff, AX
	KMOVQ AX, K2
	SHRQ $7, CX
	TESTQ CX, CX
	JZ padAVX512Done

padAVX512Chunk:
	VMOVDQU 0(SI), X2
	VMOVDQU X2, 0(DI)
	VMOVDQU 15(SI), X2
	VMOVDQU X2, 15(DI)
	MOVBLZX 31(SI), R8
	ANDL $0x3f, R8
	MOVB R8, 31(DI)

	VPMOVZXBW 32(SI), K1, Z0
	VPMOVZXBW 31(SI), K1, Z1
	VPSLLW $2, Z0, K1, Z0
	VPSRLW $6, Z1, K1, Z1
	VPORQ Z1, Z0, K1, Z0
	VPMOVWB Z0, K1, 32(DI)
	ANDB $0x3f, 63(DI)

	VPMOVZXBW 64(SI), K1, Z0
	VPMOVZXBW 63(SI), K1, Z1
	VPSLLW $4, Z0, K1, Z0
	VPSRLW $4, Z1, K1, Z1
	VPORQ Z1, Z0, K1, Z0
	VPMOVWB Z0, K1, 64(DI)
	ANDB $0x3f, 95(DI)

	VPMOVZXBW 96(SI), K2, Z0
	VPMOVZXBW 95(SI), K2, Z1
	VPSLLW $6, Z0, K1, Z0
	VPSRLW $2, Z1, K1, Z1
	VPORQ Z1, Z0, K1, Z0
	VPMOVWB Z0, K2, 96(DI)
	MOVBLZX 126(SI), R9
	SHRL $2, R9
	MOVB R9, 127(DI)

	ADDQ $127, SI
	ADDQ $128, DI
	DECQ CX
	JNZ padAVX512Chunk
	VZEROUPPER

padAVX512Done:
	RET

